const PERSONA_INDEX_BY_ID: Record<string, Energy> = {
  jason: 'analytical',
  farhan: 'chill',
  darren: 'dry',
  'maya-match': 'warm',
  'alex-match': 'chill',
  'sam-match': 'playful',
  'lena-match': 'warm',
  'new': 'warm',
};

const ENERGIES: Energy[] = ['analytical', 'chill', 'dry', 'warm', 'playful'];

function hash(seed: string) {
  let h = 0;
  for (let i = 0; i < seed.length; i += 1) h = (h * 31 + seed.charCodeAt(i)) >>> 0;
  return h;
}

function energyFor(match: Match): Energy {
  return PERSONA_INDEX_BY_ID[match.id] ?? ENERGIES[hash(`${match.id}-${match.name}`) % ENERGIES.length];
}

function fill(template: string, song: Song, playlistTitle: string): string {
  const year = song.year || 2010;
  const decade = `${Math.floor(year / 10) * 10}`.slice(-2);
  return template
    .replace(/\{title\}/g, song.title)
    .replace(/\{artist\}/g, song.artist)
    .replace(/\{album\}/g, song.album)
    .replace(/\{year\}/g, String(year))
    .replace(/\{decade\}/g, decade)
    .replace(/\{playlist\}/g, playlistTitle);
}

export function generateIncomingMessage(match: Match, playlist: Playlist): string {
  const energy = energyFor(match);
  const bank = MESSAGES[energy];
  const song = playlist.songs[Math.floor(Math.random() * playlist.songs.length)];
  if (!song) return `${match.name} just dropped into the chat.`;
  const template = bank[Math.floor(Math.random() * bank.length)];
  return fill(template, song, playlist.title);
}
